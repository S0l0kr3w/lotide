const without = function(sourceArr, removeArr) {
  let finalAnswer = [];
  for (i = 0; i < sourceArr.length; i++) {
    if (sourceArr[i] !== removeArr[i]) {
      finalAnswer.push(sourceArr[i]);
    }
  }
  return finalAnswer;
};

console.log(without([1, 2, 3], [1]));// => [2, 3]
console.log(without(["1", "2", "3"], [1, 2, "3"])); // => ["1", "2"]

